package com.linear;

public class Node {
	
	private Object data;
	private Node nextElement;
	
	public Object getData() {
		return data;
	}
	public void setData(Object data) {
		this.data = data;
	}
	public Node getNextElement() {
		return nextElement;
	}
	public void setNextElement(Node nextElement) {
		this.nextElement = nextElement;
	}

}
